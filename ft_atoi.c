#include "libft.h"

int	ft_atoi(const char *str)
{
	int					i;
	unsigned long long	result;
	int					sin;

	i = 0;
	result = 0;
	sin = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sin *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - 48;
		if (result > 9223372036854775807 && sin == -1)
			return (0);
		else if (result >= 9223372036854775807 && sin == 1)
			return (-1);
		i++;
	}
	return (result * sin);
}
